package Manha.entidades;

public class Estudante {
    private String nome;
    private int idade;
    private int matricula;
    private String serie;

    public Estudante(String nome, int idade, int matricula, String serie){
        this.nome = nome;
        this.idade = idade;
        this.matricula = matricula;
        this.serie = serie;

    }
}
